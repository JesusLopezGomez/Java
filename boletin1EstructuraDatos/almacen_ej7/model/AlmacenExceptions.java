package boletin1EstructuraDatos.almacen_ej7.model;

public class AlmacenExceptions extends Exception {

	public AlmacenExceptions() {
	}

	public AlmacenExceptions(String message) {
		super(message);
	}

	public AlmacenExceptions(Throwable cause) {
		super(cause);
	}

	public AlmacenExceptions(String message, Throwable cause) {
		super(message, cause);
	}

	public AlmacenExceptions(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

}
