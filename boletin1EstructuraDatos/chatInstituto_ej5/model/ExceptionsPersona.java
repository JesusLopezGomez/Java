package boletin1EstructuraDatos.chatInstituto_ej5.model;

public class ExceptionsPersona extends Exception {

	public ExceptionsPersona() {
	}

	public ExceptionsPersona(String message) {
		super(message);
	}

	public ExceptionsPersona(Throwable cause) {
		super(cause);
	}

	public ExceptionsPersona(String message, Throwable cause) {
		super(message, cause);
	}

	public ExceptionsPersona(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

}
