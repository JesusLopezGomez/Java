package boletin1EstructuraDatos.historialNavegacion_ej4;

public class ExceptionHistorial extends Exception {

	public ExceptionHistorial() {
	}

	public ExceptionHistorial(String message) {
		super(message);
	}

	public ExceptionHistorial(Throwable cause) {
		super(cause);
	}

	public ExceptionHistorial(String message, Throwable cause) {
		super(message, cause);
	}

	public ExceptionHistorial(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

}
